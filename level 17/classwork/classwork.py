# ფუნქცია, რომელიც გამოიტანს ორი რიცხვის ჯამს
def sum_numbers(a, b):
    print(a + b)


# ფუნქცია, რომელიც ამოწმებს არის თუ არა ასაკი > 18
def check_age(age):
    if age > 18:
        print(True)
    else:
        print(False)


# ფუნქციების გამოძახება (სურვილის მიხედვით)
sum_numbers(5, 7)
